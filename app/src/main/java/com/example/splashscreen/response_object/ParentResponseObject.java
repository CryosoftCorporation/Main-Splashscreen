package com.example.splashscreen.response_object;

public class ParentResponseObject {
    private int record_id;
    private String parentname;
    private String parentmail;
    private String parentpassword;
    private String parentcontact;
    private String parentchild;
    private String parentaddress;

    public int getRecord_id() {
        return record_id;
    }

    public void setRecord_id(int record_id) {
        this.record_id = record_id;
    }

    public String getParentname() {
        return parentname;
    }

    public void setParentname(String parentname) {
        this.parentname = parentname;
    }

    public String getParentmail() {
        return parentmail;
    }

    public void setParentmail(String parentmail) {
        this.parentmail = parentmail;
    }

    public String getParentpassword() {
        return parentpassword;
    }

    public void setParentpassword(String parentpassword) {
        this.parentpassword = parentpassword;
    }

    public String getParentcontact() {
        return parentcontact;
    }

    public void setParentcontact(String parentcontact) {
        this.parentcontact = parentcontact;
    }

    public String getParentchild() {
        return parentchild;
    }

    public void setParentchild(String parentchild) {
        this.parentchild = parentchild;
    }

    public String getParentaddress() {
        return parentaddress;
    }

    public void setParentaddress(String parentaddress) {
        this.parentaddress = parentaddress;
    }
}
